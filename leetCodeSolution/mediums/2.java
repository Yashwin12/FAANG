/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public ListNode addTwoNumbers(ListNode l1, ListNode l2) {
        if (l1 == null && l2 == null)
            return null;

        if (l1 == null)
            return l2;

        if (l2 == null)
            return l1;

        int carry = 0;

        ListNode dummy = new ListNode(-1);
        ListNode finalLL = dummy;

        while (l1 != null || l2 != null) {

            int sum = carry;

            if (l1 != null) {
                sum += l1.val;
                l1 = l1.next;
            }

            if (l2 != null) {
                sum += l2.val;
                l2 = l2.next;
            }

            carry = sum > 9 ? 1 : 0;

            int acutalValue = sum % 10;

            finalLL.next = new ListNode(acutalValue);

            finalLL = finalLL.next;

        }

        if (carry == 1)
            finalLL.next = new ListNode(carry);

        return dummy.next;

    }
}